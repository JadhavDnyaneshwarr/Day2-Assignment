package com.bridgelabz.assignment;

public class VowelOrConstant_SwitchCase {
    public static void main(String[] args) {
        char ch = 't';
        switch (ch){
            case 'a':
                System.out.println("the alphabet is vowel");
                break;

            case 'e':
                System.out.println("the alphabet is vowel");
                break;

            case 'i':
                System.out.println("the alphabet is vowel");
                break;

            case 'o':
                System.out.println("the alphabet is vowel");
                break;

            case 'u':
                System.out.println("the alphabet is vowel");
                break;
            default:
                System.out.println("the alphabet is constant");
        }
    }
}
